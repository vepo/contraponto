ALLOW_SAME_AS_EN = frozenset({
    "locale.en",
    "locale.es",
    "locale.pt",
    "rss.link",
    "locale.name.en",
    "locale.name.es",
    "locale.name.ptBr",
    "dashboard.blogSelector",
    "hub.blogs",
})


def main() -> None:
    with EN_PATH.open(encoding="utf-8") as f:
        en = json.load(f)

    en["locale.name.ptBr"] = "Português (Brasil)"
    en["locale.name.en"] = "English"
    en["locale.name.es"] = "Español"

    missing = [k for k in en if k not in ES_VALUES]
    if missing:
        raise SystemExit(f"Missing Spanish translations for: {missing}")

    es = {k: ES_VALUES[k] for k in en}

    with EN_PATH.open("w", encoding="utf-8") as f:
        json.dump(en, f, indent=2, ensure_ascii=False)
        f.write("\n")

    with ES_PATH.open("w", encoding="utf-8") as f:
        json.dump(es, f, indent=2, ensure_ascii=False)
        f.write("\n")

    same = [k for k in en if es[k] == en[k] and k not in ALLOW_SAME_AS_EN]
    print(f"Wrote {len(es)} keys. Still identical to EN (excluding allowlist): {len(same)}")
    if same:
        raise SystemExit(f"Untranslated keys: {same[:20]}")


if __name__ == "__main__":
    main()
