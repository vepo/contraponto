CREATE TABLE tb_users (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    username      VARCHAR(15) NOT NULL UNIQUE,
    email         VARCHAR(255) NOT NULL UNIQUE,
    name          VARCHAR(255) NOT NULL,
    password_hash VARCHAR(255) NOT NULL,
    active        BOOLEAN NOT NULL DEFAULT TRUE,
    created_at    TIMESTAMP(6) NOT NULL DEFAULT NOW(),
    updated_at    TIMESTAMP(6) NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_users_email    ON tb_users(email);
CREATE INDEX idx_users_username ON tb_users(username);

CREATE TABLE tb_user_roles (
    user_id BIGINT NOT NULL,
    role    VARCHAR(20) NOT NULL,
    CONSTRAINT pk_user_roles PRIMARY KEY (user_id, role),
    CONSTRAINT fk_user_roles_user FOREIGN KEY (user_id) REFERENCES tb_users(id) ON DELETE CASCADE
);

CREATE TABLE tb_images (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid VARCHAR(36) NOT NULL UNIQUE,
    filename VARCHAR(255) NOT NULL,
    content_type VARCHAR(100) NOT NULL,
    size BIGINT NOT NULL,
    file_path VARCHAR(500) NOT NULL,
    url VARCHAR(500) NOT NULL,
    active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMP(6) NOT NULL
);

CREATE INDEX idx_images_uuid ON tb_images(uuid);
CREATE INDEX idx_images_active ON tb_images(active);
CREATE INDEX idx_images_created_at ON tb_images(created_at);

CREATE TABLE tb_blogs (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY,
    name        VARCHAR(255) NOT NULL,
    slug        VARCHAR(255) NOT NULL,
    description TEXT,
    owner_id    BIGINT NOT NULL,
    main        BOOLEAN NOT NULL DEFAULT false,
    active      BOOLEAN NOT NULL DEFAULT true,
    created_at  TIMESTAMP NOT NULL,

    CONSTRAINT pk_tb_blogs PRIMARY KEY (id),
    CONSTRAINT fk_blog_owner FOREIGN KEY (owner_id) REFERENCES tb_users(id),
    CONSTRAINT uq_blog_owner_slug UNIQUE (owner_id, slug)
);

