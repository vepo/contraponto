CREATE TABLE tb_images (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    uuid VARCHAR(36) NOT NULL UNIQUE,
    filename VARCHAR(255) NOT NULL,
    content_type VARCHAR(100) NOT NULL,
    size BIGINT NOT NULL,
    file_path VARCHAR(500) NOT NULL,
    url VARCHAR(500) NOT NULL,
    active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMP(6) NOT NULL
);

CREATE INDEX idx_images_uuid ON tb_images(uuid);
CREATE INDEX idx_images_active ON tb_images(active);
CREATE INDEX idx_images_created_at ON tb_images(created_at);

CREATE TABLE tb_posts (
    id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    slug              VARCHAR(255) NOT NULL,
    author_id         BIGINT NOT NULL,
    title             VARCHAR(255),
    cover_id          BIGINT,
    description       VARCHAR(512) NOT NULL,
    content           TEXT NOT NULL,
    published         BOOLEAN NOT NULL DEFAULT FALSE,
    created_at        TIMESTAMP(6) NOT NULL,
    published_at      TIMESTAMP(6),
    updated_at        TIMESTAMP(6) NOT NULL,

    CONSTRAINT fk_posts_author FOREIGN KEY (author_id) REFERENCES tb_users(id),
    CONSTRAINT fk_posts_cover_image FOREIGN KEY (cover_id) REFERENCES tb_images(id),
    CONSTRAINT uk_posts_slug_user UNIQUE (slug, author_id)
);

CREATE TABLE tb_views (
    id BIGSERIAL PRIMARY KEY,
    post_id BIGINT NOT NULL REFERENCES tb_posts(id) ON DELETE CASCADE,
    user_id BIGINT,
    session_id VARCHAR(255) NOT NULL,
    viewed_at TIMESTAMP NOT NULL,
    CONSTRAINT fk_views_user FOREIGN KEY (user_id) REFERENCES tb_users(id),
    CONSTRAINT fk_views_post FOREIGN KEY (post_id) REFERENCES tb_posts(id)
);