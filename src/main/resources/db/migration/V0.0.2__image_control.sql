-- Backfill blog_id from post covers
UPDATE tb_images i
SET blog_id = (
    SELECT p.blog_id FROM tb_posts p WHERE p.cover_id = i.id LIMIT 1
)
WHERE blog_id IS NULL
  AND EXISTS (SELECT 1 FROM tb_posts p WHERE p.cover_id = i.id);

UPDATE tb_images i
SET blog_id = (
    SELECT p.blog_id
    FROM tb_post_publications pp
    JOIN tb_posts p ON p.id = pp.post_id
    WHERE pp.cover_id = i.id
    LIMIT 1
)
WHERE blog_id IS NULL
  AND EXISTS (
    SELECT 1 FROM tb_post_publications pp WHERE pp.cover_id = i.id
);

-- Remaining orphans: assign to first active main blog (dev seed / legacy data)
UPDATE tb_images i
SET blog_id = (SELECT b.id FROM tb_blogs b WHERE b.main = true AND b.active = true ORDER BY b.id LIMIT 1)
WHERE blog_id IS NULL;

