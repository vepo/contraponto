CREATE TABLE tb_post_image_dependencies (
    id       BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    post_id  BIGINT NOT NULL,
    image_id BIGINT NOT NULL,
    role     VARCHAR(20) NOT NULL,
    CONSTRAINT fk_post_image_dep_post FOREIGN KEY (post_id) REFERENCES tb_posts(id) ON DELETE CASCADE,
    CONSTRAINT fk_post_image_dep_image FOREIGN KEY (image_id) REFERENCES tb_images(id) ON DELETE CASCADE,
    CONSTRAINT uq_post_image_dep UNIQUE (post_id, image_id, role)
);

CREATE INDEX idx_post_image_dep_image ON tb_post_image_dependencies(image_id);

